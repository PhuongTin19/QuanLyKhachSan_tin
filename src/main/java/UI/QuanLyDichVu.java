    public boolean valida_data() {
        String patternSo = "[0-9 ]+";
        String patternString = "[a-zA-Z ]+";
        //TÊN DỊCH VỤ
        if (txtTenDV.getText().equals("")) {
            lblBatLoiTenDV.setText("Tên dịch vụ không được bỏ trống");
            lblBatLoiTenDV.setForeground(Color.red);
            lblBatLoiTenDV.requestFocus();
            return false;
        } else {
            lblBatLoiTenDV.setText("");
        }
        if (txtTenDV.getText().matches(patternSo)) {
            lblBatLoiTenDV.setText("Tên dịch vụ phải là chữ");
            lblBatLoiTenDV.setForeground(Color.red);
            lblBatLoiTenDV.requestFocus();
            return false;
        } else {
            lblBatLoiTenDV.setText("");
        }
        // GIÁ DICH VỤ
        if (txtGia.getText().equals("")) {
            lblBatLoiGia.setText("Giá dịch vụ không được bỏ trống");
            lblBatLoiGia.setForeground(Color.red);
            lblBatLoiGia.requestFocus();
            return false;
        } else {
            lblBatLoiGia.setText("");
        }
        if (txtGia.getText().matches(patternString)) {
            lblBatLoiGia.setText("Giá dịch vụ phải là số");
            lblBatLoiGia.setForeground(Color.red);
            lblBatLoiGia.requestFocus();
            return false;
        } else {
            lblBatLoiGia.setText("");
        }
        String giadv = txtGia.getText();
        try {
            float price = Float.parseFloat(giadv);
            if (price < 0 || price > 3000000) {
                lblBatLoiGia.setText("Giá dịch vụ phải từ 200000 - 3000000");
                return false;
            } else {
                lblBatLoiGia.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return true;
    }

    private boolean CheckTrungMa() {
        if (dichVu.selectID(txtMaDichVu.getText()) != null) {
            lblBatLoiDV.setText("Mã dịch vụ đã tồn tại");
            lblBatLoiDV.setForeground(Color.red);
            lblBatLoiDV.requestFocus();
            return false;
        } else {
            lblBatLoiDV.setText("");
        }
        return true;
    }

