    private void updateStatus() {
        boolean edit = this.Index >= 0;
        btnThem.setEnabled(!edit);
        btnCapNhat.setEnabled(edit);
        btnXoa.setEnabled(edit);
    }

    private void insert() {
        DichVu nv = getForm();
        try {
            dichVu.insert(txtMaDichVu.getText(), txtTenDV.getText(), Float.valueOf(txtGia.getText()));
            load();
            clear();
            MsgBox.alert(this, "Đã thêm dịch vụ vào danh sách");
        } catch (Exception e) {
            MsgBox.alert(this, "Mã dịch vụ đã tồn tại.Thêm thất bại");
            e.printStackTrace();
        }
    }

    private void update() {
        DichVu dv = getForm();
        try {
            dichVu.update(txtTenDV.getText(), Float.valueOf(txtGia.getText()), txtMaDichVu.getText());
            load();
            MsgBox.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại");
            e.printStackTrace();
        }
    }

    private void delete() {
        String madv = txtMaDichVu.getText();
        if (MsgBox.confirm(this, "Bạn có muốn xóa dịch vụ này không?.")) {
            try {
                dichVu.delete(madv);
                load();
                clear();
                MsgBox.alert(this, "Đã xóa dịch vụ khỏi danh sách");
            } catch (Exception e) {
                MsgBox.alert(this, "Dịch vụ chưa được xóa khỏi danh sách.Xóa thất bại");
            }
        }
    }

    private void clear() {
        DichVu dv = new DichVu();
        Random rd = new Random();
        String madv = "DV" + (1 + rd.nextInt(99));
        dv.setMaDV(madv);
        lblBatLoiDV.setText("");
        lblBatLoiGia.setText("");
        lblBatLoiTenDV.setText("");
        setForm(dv);
        this.Index = -1;
        updateStatus();
    }

    private void TimKiem() {
        load();
        updateStatus();
    }
