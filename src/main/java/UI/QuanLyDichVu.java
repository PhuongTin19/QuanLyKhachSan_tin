    //Đổ dữ liệu lên bảng
    public void load() {
        DefaultTableModel model = (DefaultTableModel) tblDichVu.getModel();
        model.setRowCount(0);
        try {
            List<DichVu> nv = dichVu.selecbyKeyword(txtTimKiem.getText());
            for (DichVu dichVu : nv) {
                Object[] row = {
                    dichVu.getMaDV(),
                    dichVu.getTenDV(),
                    dichVu.getGia()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
            e.printStackTrace();
        }
    }
