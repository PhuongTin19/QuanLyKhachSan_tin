    private void load() {
        DefaultTableModel model = (DefaultTableModel) tblPhong.getModel();
        model.setRowCount(0);
        //List<Phong> list = pdao.selectAll();
        List<Phong> list = pdao.selectbyKeyword(txtTimKiem.getText());
        for (Phong p : list) {
            Object[] row = new Object[]{
                p.getMaPhong(),
                p.getTenPhong(),
                p.getLoaiPhong(),
                p.getGiaPhong(),
                p.getTinhTrang(),
                p.getMaNV(),};
            model.addRow(row);
        }
    }

