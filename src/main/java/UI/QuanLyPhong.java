    private void setForm(Phong p) {
        txtMaPhong.setText(p.getMaPhong());
        txtTenPhong.setText(p.getTenPhong());
        txtMaNV.setText(p.getMaNV());
        lblTinhTrang.setText(p.getTinhTrang());
        if (p.getLoaiPhong().equalsIgnoreCase("Giường đơn nhỏ")) {
            cboLoaiPhong.setSelectedIndex(0);
        } else if (p.getLoaiPhong().equalsIgnoreCase("Giường đơn lớn")) {
            cboLoaiPhong.setSelectedIndex(1);
        } else if (p.getLoaiPhong().equalsIgnoreCase("Giường đôi nhỏ")) {
            cboLoaiPhong.setSelectedIndex(2);
        } else if (p.getLoaiPhong().equalsIgnoreCase("Giường đôi lớn")) {
            cboLoaiPhong.setSelectedIndex(3);
        } else {
            cboLoaiPhong.setSelectedIndex(4);
        }
        if (p.getGiaPhong() == 1000000) {
            cboGiaPhong.setSelectedIndex(0);
        } else if (p.getGiaPhong() == 1500000) {
            cboGiaPhong.setSelectedIndex(1);
        } else if (p.getGiaPhong() == 2000000) {
            cboGiaPhong.setSelectedIndex(2);
        } else if (p.getGiaPhong() == 2500000) {
            cboGiaPhong.setSelectedIndex(3);
        } else {
            cboGiaPhong.setSelectedIndex(4);
        }

    }

