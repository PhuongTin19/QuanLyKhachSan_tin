    private void fillTableDoanhThu(int selecttype) {
        DefaultTableModel model = (DefaultTableModel) tblThongKe.getModel();
        model.setRowCount(0);
        // 0 LÀ NĂM
        if (selecttype == 0) {
            int year = (Integer) cboYear.getSelectedItem();
            //int day = (Integer) cboDay.getSelectedItem();
            List<Object[]> list = tkDao.getDoanhThu(0, year, 0, 0);
            for (Object[] row : list) {
                model.addRow(row);
            }
        }
        // 1 LÀ  THÁNG
        if (selecttype == 1) {
            int year = (Integer) cboYear.getSelectedItem();
            int month = (Integer) cboMonth.getSelectedIndex();
            System.out.println(month);

            List<Object[]> list = tkDao.getDoanhThu(1, year, month + 1, 0);
            for (Object[] row : list) {
                model.addRow(row);
            }
        }
        // 2 LÀ  DAY
        if (selecttype == 2) {
            int year = (Integer) cboYear.getSelectedItem();
            int month = (Integer) cboMonth.getSelectedIndex();
            int day = (Integer) cboDay.getSelectedIndex();
            List<Object[]> list = tkDao.getDoanhThu(2, year, month + 1, day + 1);
            for (Object[] row : list) {
                model.addRow(row);
            }
        }
    }

