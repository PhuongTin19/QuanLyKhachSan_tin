    private void exportExcel(JTable nametable, File file) {
        XSSFWorkbook wb = new XSSFWorkbook();
        XSSFSheet sheet = wb.createSheet("THỐNG KÊ DOANH THU");
        //TẠO TIÊU ĐỀ CHO TRANG EXECL
        createTitle(wb, sheet, nametable);
        // TẠO ĐẦU TRANG
        createHeader(wb, sheet);
        insertData(sheet, nametable);
        setColumnWidth(sheet);
        saveSheet(wb, file);
    }
    //Lưu file
    private void saveSheet(XSSFWorkbook wb, File file) {
        try {
            FileOutputStream out = new FileOutputStream(file);
            wb.write(out);
            wb.close();
            out.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    //set bề ngang của mỗi cột
    private void setColumnWidth(XSSFSheet sheet) {
        sheet.setColumnWidth(0, 7000);
        sheet.setColumnWidth(1, 7000);
        sheet.setColumnWidth(2, 7000);
        sheet.setColumnWidth(3, 7000);
        sheet.setColumnWidth(4, 7000);
        sheet.setColumnWidth(5, 7000);
    }

    //Thêm dữ liệu vào các cột
    private void insertData(XSSFSheet sheet, JTable nametable) {
        int currentRow=4;
        for (int j = 0; j < nametable.getRowCount(); j++) {
            Row row = sheet.createRow(currentRow++);
            for (int k = 0; k < nametable.getColumnCount(); k++) {
                Cell cell = row.createCell(k);
                Object value=nametable.getValueAt(j, k);
                String content="";
                if (value != null) {
                    content=value.toString();
                    
                }
                cell.setCellValue(content);
            }
        }
    }
    
    //set ngày
    private String getCurentDateasString() {
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss");
        return format.format(new Date());
    }
    
    //Tạo tiêu đề cho mỗi cột
    private void createTitle(XSSFWorkbook xb, XSSFSheet sheet, JTable table) {
        XSSFRow row = sheet.createRow(3);
        XSSFCellStyle style = xb.createCellStyle();
        XSSFFont font = xb.createFont();
        font.setFontName("Time New Roman");
        font.setFontHeightInPoints((short) 12);
        font.setBold(true);
        style.setFont(font);
        style.setAlignment(HorizontalAlignment.CENTER);
        for (int i = 0; i < table.getColumnCount(); i++) {
            Cell cell = row.createCell(i);
            cell.setCellValue(table.getColumnName(i));
            row.getCell(i).setCellStyle(style);
        }
    }
    
    //Tạo phần tiêu đề 
    private void createHeader(XSSFWorkbook xb, XSSFSheet sheet) {
        XSSFRow row = sheet.createRow(0);
        XSSFCellStyle style = xb.createCellStyle();
        XSSFFont font = xb.createFont();
        font.setFontName("Time New Roman");
        font.setFontHeightInPoints((short) 12);
        font.setBold(true);
        font.setItalic(true);
        style.setFont(font);
        style.setWrapText(true);
        style.setAlignment(HorizontalAlignment.CENTER);
        style.setVerticalAlignment(VerticalAlignment.CENTER);
        Cell cell = row.createCell(0);
        cell.setCellValue("Danh sách thống kê" + "\nNgày lập báo cáo:" + getCurentDateasString());
        cell.setCellStyle(style);

        sheet.addMergedRegion(new CellRangeAddress(0, 2, 0, 6));

    }

